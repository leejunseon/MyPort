-- TBL_USER Table Create SQL
CREATE TABLE TBL_USER
(
    U_NO            NUMBER(18, 0)    NOT NULL,
    U_ID            VARCHAR2(20)     NOT NULL,
    U_PW            VARCHAR2(20)     NOT NULL,
    U_NAME          VARCHAR2(20)     NOT NULL,
    U_EMAIL         VARCHAR2(20)     NOT NULL,
    U_REGDATE       VARCHAR2(40)     NOT NULL,
    U_UPDATEDATE    VARCHAR2(40)     NOT NULL,
    CONSTRAINT TBL_USER_PK PRIMARY KEY (U_NO),
    CONSTRAINT TBL_USER_UK UNIQUE (U_ID)
)
/
ALTER TABLE TBL_USER
    MODIFY U_REGDATE DEFAULT to_char(sysdate, 'YYYY/MM/DD HH24:MI:SS')
/
ALTER TABLE TBL_USER
    MODIFY U_UPDATEDATE DEFAULT to_char(sysdate, 'YYYY/MM/DD HH24:MI:SS')
/

CREATE SEQUENCE SEQ_USER
    START WITH 1
    INCREMENT BY 1;
/

CREATE OR REPLACE TRIGGER TBL_USER_AI_TRG
    BEFORE INSERT ON TBL_USER
    REFERENCING NEW AS NEW FOR EACH ROW
BEGIN
    SELECT SEQ_USER.NEXTVAL
    INTO :NEW.U_NO
    FROM DUAL;
END;
/

COMMENT ON TABLE TBL_USER IS '유저'
/

COMMENT ON COLUMN TBL_USER.U_NO IS '유저 키'
/

COMMENT ON COLUMN TBL_USER.U_ID IS '유저 아이디'
/

COMMENT ON COLUMN TBL_USER.U_PW IS '유저 패스워드'
/

COMMENT ON COLUMN TBL_USER.U_NAME IS '유저 이름'
/

COMMENT ON COLUMN TBL_USER.U_EMAIL IS '유저 이메일'
/

COMMENT ON COLUMN TBL_USER.U_REGDATE IS '유저 등록 날짜/시간'
/

COMMENT ON COLUMN TBL_USER.U_UPDATEDATE IS '유저 업데이트 날짜/시간'
