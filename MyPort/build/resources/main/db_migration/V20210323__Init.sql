-- TAB_USER Table Create SQL
CREATE TABLE TAB_USER
(
    U_PK       INT             NOT NULL,
    U_ID       VARCHAR2(20)    NOT NULL,
    U_PW       VARCHAR2(20)    NOT NULL,
    U_NAME     VARCHAR2(20)    NOT NULL,
    U_EMAIL    VARCHAR2(20)    NOT NULL,
    CONSTRAINT TAB_USER_PK PRIMARY KEY (U_PK)
)
    /

CREATE SEQUENCE TAB_USER_SEQ
    START WITH 1
    INCREMENT BY 1;
/

CREATE OR REPLACE TRIGGER TAB_USER_AI_TRG
BEFORE INSERT ON TAB_USER
REFERENCING NEW AS NEW FOR EACH ROW
BEGIN
SELECT TAB_USER_SEQ.NEXTVAL
INTO :NEW.U_PK
FROM DUAL;
END;
/

--DROP TRIGGER TAB_USER_AI_TRG;
/

--DROP SEQUENCE TAB_USER_SEQ;
/


